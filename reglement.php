        Si le fromage ne contient pas de ticket alors cela signifie que le consommateur n’a pas gagné.
        Si le fromage contient un ticket avec un code unique alors le consommateur a gagné.
        Pour découvrir combien le Participant a gagné, ce dernier doit :
